
num1 = Number(prompt("ingresa el numero de la semana"));

switch (num1) {
    case 0:
        console.log('No corresponde a un dia de la semana')
        break;
    case 1:
        console.log('Lunes')
        break;
    case 2:
        console.log('Martes')
        break;
    case 3:
        console.log('Miercoles')
        break;
    default:
        break;
}
